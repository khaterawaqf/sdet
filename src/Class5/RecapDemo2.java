package Class5;

public class RecapDemo2 {
    public static void main(String[] args) {
         for (int i= 10; i>=2 ; i-=2){
             System.out.println(i);

        }
         for (int i = 0; i < 10; i++ ){
             if ( i==3){
                 break;
             }
             System.out.println(i);

         }

    }
}
