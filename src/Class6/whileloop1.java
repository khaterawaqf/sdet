package Class6;

public class whileloop1 {
    public static void main(String[] args) {

        int n=1;
        while (n<=100){
            System.out.println(n);
            n++;
        }
        System.out.println("**************************");

        int m=1;
        while (m<=15){
            System.out.println(m);
            m+=2;

        }
        System.out.println("**************************");

        int w=5;
        while (w<=50){
            System.out.println(w);
            w+=5;
        }
        System.out.println("**************************");

    }
}
